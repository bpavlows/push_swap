#include "push_swap.h"

void	sort_stacks(t_stack **a, t_stack **b)
{
	int		len_a;
	t_stack	*min_node;

	len_a = stack_len(*a);
	if (len_a-- > 3 && !stack_sorted(*a))
		pb(b, a);
	if (len_a-- > 3 && !stack_sorted(*a))
		pb(b, a);
	while (len_a-- > 3 && !stack_sorted(*a))
	{
		init_nodes_a(*a, *b);
		move_a_to_b(a, b);
	}
	sort_three(a);
	while (*b)
	{
		init_nodes_b(*a, *b);
		prep_for_push(a, (*b)->target_node, 'a');
		pa(a, b);
	}
	current_index(*a);
	min_node = find_min(*a);
	prep_for_push(a, min_node, 'a');
}
